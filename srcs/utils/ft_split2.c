#include "push_swap.h"

char	*ft_cpy2(int len, char *ret, char *start)
{
	int	i;

	i = 0;
	while (len--)
	{
		ret[i] = start[i];
		i++;
	}
	ret[i] = '\0';
	return (ret);
}
